//Exercise 1.14: Compare and contrast the loops that used a for with those
//using a while. Are there advantages or disadvantages to using either form?

#include <iostream>

int main() {
    // in general, we use the for() loop when we know the number of iterations in advance,
    // and we use the while() loop when there is an unknown number of potential iterations.
    // the for() loop gives our code more conciseness,
    // as we can initialize and increment our index variable in a single line.
    // a while() loop will evaluate a condition first, then iterate.
    // a do... while() loop will always perform the given operations at least once before checking the condition.
    return 0;
}
